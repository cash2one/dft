package com.fruit.query.report;

public class SubTitle {
	private String leftExp;
	private String centerExp;
	private String rightExp;
	private String lStyle;
	private String cStyle;
	private String rStyle;
	
	public String getlStyle() {
		return lStyle;
	}
	public void setlStyle(String lStyle) {
		this.lStyle = lStyle;
	}
	public String getcStyle() {
		return cStyle;
	}
	public void setcStyle(String cStyle) {
		this.cStyle = cStyle;
	}
	public String getrStyle() {
		return rStyle;
	}
	public void setrStyle(String rStyle) {
		this.rStyle = rStyle;
	}
	public String getLeftExp() {
		return leftExp;
	}
	public void setLeftExp(String leftExp) {
		this.leftExp = leftExp;
	}
	public String getCenterExp() {
		return centerExp;
	}
	public void setCenterExp(String centerExp) {
		this.centerExp = centerExp;
	}
	public String getRightExp() {
		return rightExp;
	}
	public void setRightExp(String rightExp) {
		this.rightExp = rightExp;
	}
}
